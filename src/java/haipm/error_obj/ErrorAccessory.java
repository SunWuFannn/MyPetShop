package haipm.error_obj;

/**
 *
 * @author 99hai
 */
public class ErrorAccessory {

    private String errID, errName, errPrice, errImage, errTypeID, errQuantity;

    public String getErrID() {
        return errID;
    }

    public void setErrID(String errID) {
        this.errID = errID;
    }

    public String getErrName() {
        return errName;
    }

    public void setErrName(String errName) {
        this.errName = errName;
    }

    public String getErrPrice() {
        return errPrice;
    }

    public void setErrPrice(String errPrice) {
        this.errPrice = errPrice;
    }

    public String getErrImage() {
        return errImage;
    }

    public void setErrImage(String errImage) {
        this.errImage = errImage;
    }

    public String getErrTypeID() {
        return errTypeID;
    }

    public void setErrTypeID(String errTypeID) {
        this.errTypeID = errTypeID;
    }

    public String getErrQuantity() {
        return errQuantity;
    }

    public void setErrQuantity(String errQuantity) {
        this.errQuantity = errQuantity;
    }

}
